package kr.nomad.mars.dto;

public class MagazinePage {
	
	int pSeq = 0;
	int mSeq = 0;
	int page = 0;
	String pageTitle = "";
	String pageContents = "";
	String pageFilename = "";
	String pageThumname = "";
	String regDate ="";
	
	
	
	public String getRegDate() {
		return regDate;
	}
	public void setRegDate(String regDate) {
		this.regDate = regDate;
	}
	public int getpSeq() {
		return pSeq;
	}
	public void setpSeq(int pSeq) {
		this.pSeq = pSeq;
	}
	public int getmSeq() {
		return mSeq;
	}
	public void setmSeq(int mSeq) {
		this.mSeq = mSeq;
	}
	public int getPage() {
		return page;
	}
	public void setPage(int page) {
		this.page = page;
	}
	public String getPageTitle() {
		return pageTitle;
	}
	public void setPageTitle(String pageTitle) {
		this.pageTitle = pageTitle;
	}
	public String getPageContents() {
		return pageContents;
	}
	public void setPageContents(String pageContents) {
		this.pageContents = pageContents;
	}
	public String getPageFilename() {
		return pageFilename;
	}
	public void setPageFilename(String pageFilename) {
		this.pageFilename = pageFilename;
	}
	public String getPageThumname() {
		return pageThumname;
	}
	public void setPageThumname(String pageThumname) {
		this.pageThumname = pageThumname;
	}
	
	

}
