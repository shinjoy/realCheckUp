package kr.nomad.mars.dto;

import java.util.ArrayList;
import java.util.List;

public class Magazine {
	
	int mSeq = 0;
	String contents = "";
	String type = "";
	String title = "";
	String fileName = "";
	String subTitle = "";
	String thumFile = "";
	String regDate ="";
	String typetxt="";
	
	
	
	public String getTypetxt() {
		String txt="";
		
			if(this.type.equals(Config.DISEASE_BLOODSUGAR)){
				txt="혈당";
			}
			if(this.type.equals(Config.DISEASE_ACTIVITY)){
				txt="활동량";
			}
			if(this.type.equals(Config.DISEASE_CHOLESTEROL)){
				txt="콜레스테롤";
			}
			if(this.type.equals(Config.DISEASE_DRINK)){
				txt="음주";
			}
			if(this.type.equals(Config.DISEASE_PRESSURE)){
				txt="혈압";
			}
			if(this.type.equals(Config.DISEASE_SMOKE)){
				txt="흡연";
			}
			if(this.type.equals(Config.DISEASE_WEIGHT)){
				txt="체중";
			}
		
		
		return txt;
	}
	public String getRegDate() {
		return regDate;
	}
	public void setRegDate(String regDate) {
		this.regDate = regDate;
	}
	public int getmSeq() {
		return mSeq;
	}
	public void setmSeq(int mSeq) {
		this.mSeq = mSeq;
	}
	public String getContents() {
		return contents;
	}
	public void setContents(String contents) {
		this.contents = contents;
	}
	
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public String getSubTitle() {
		return subTitle;
	}
	public void setSubTitle(String subTitle) {
		this.subTitle = subTitle;
	}
	public String getThumFile() {
		return thumFile;
	}
	public void setThumFile(String thumFile) {
		this.thumFile = thumFile;
	}
	

  

}
